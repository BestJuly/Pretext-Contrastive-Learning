        weight_l = weight_l.view(batchSize, K + 1, inputSize)
        ## generate pesudo hard-negative features
        weight_l_pos = weight_l[:,0,:] # [bs, dim]
        weight_l_pos = weight_l_pos.unsqueeze(1)
        weight_l_pos = weight_l_pos.repeat(1, self.K, 1) # [bs, k, dim]
        weight_l_neg = weight_l[:,1:,:] # [bs, k, dim]
        weight_l_hardneg = (weight_l_pos + weight_l_neg) / 2
        weight_l = torch.cat((weight_l, weight_l_hardneg), 1)
